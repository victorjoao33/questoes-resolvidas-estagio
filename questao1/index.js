let INDICE = 13, SOMA = 0, K = 0;
while (K < INDICE) {
    K = K + 1;
    SOMA = SOMA + K;
}
console.log("A soma dos números de 1 a 13 é: " + SOMA);

/*O código declara as variáveis INDICE, SOMA e K com os valores iniciais indicados na questão. Em seguida, o loop while é iniciado com a condição de que K seja menor que INDICE.

Dentro do loop, K é incrementado em 1 e SOMA é atualizado somando-se K ao valor anterior de SOMA. O loop continua até que K seja igual a INDICE.

Após a saída do loop, o valor da variável SOMA é impresso no console utilizando o método console.log().

Ao executar o código, a mensagem "A soma dos números de 1 a 13 é: 91" será impressa no console.*/